# ── 1. Line Chart ────────────────────────────────────────────
def draw_line_plot():
    fig, ax = plt.subplots(figsize=(18, 6))
    data = df.copy()
    ax.plot(data.index, data["value"], color="#e04f4f", linewidth=0.8)
    ax.set_title("Daily freeCodeCamp Forum Page Views 5/2016-12/2019", fontsize=14)
    ax.set_xlabel("Date")
    ax.set_ylabel("Page Views")
    ax.xaxis.set_major_locator(mticker.MaxNLocator(nbins=8))
    fig.tight_layout()
    fig.savefig("line_plot.png", dpi=150)
    print("Saved: line_plot.png")
    return fig

# ── 2. Bar Chart ─────────────────────────────────────────────
def draw_bar_plot():
    data = df.copy()
    data["year"]  = data.index.year
    data["month"] = data.index.month

    monthly_avg = (
        data.groupby(["year", "month"])["value"]
        .mean()
        .unstack()
    )
    monthly_avg.columns = [
        "January", "February", "March", "April", "May", "June",
        "July", "August", "September", "October", "November", "December"
    ]

    fig, ax = plt.subplots(figsize=(14, 7))
    monthly_avg.plot(kind="bar", ax=ax, width=0.85)
    ax.set_title("Average Daily Page Views per Month (grouped by Year)")
    ax.set_xlabel("Years")
    ax.set_ylabel("Average Page Views")
    ax.legend(title="Months", bbox_to_anchor=(1.01, 1), loc="upper left", fontsize=8)
    ax.tick_params(axis="x", rotation=0)
    fig.tight_layout()
    fig.savefig("bar_plot.png", dpi=150)
    print("Saved: bar_plot.png")
    return fig

# ── 3. Box Plots ─────────────────────────────────────────────
def draw_box_plot():
    data = df.copy()
    data.reset_index(inplace=True)
    data["year"]       = data["date"].dt.year
    data["month_abbr"] = data["date"].dt.strftime("%b")

    month_order = ["Jan","Feb","Mar","Apr","May","Jun",
                   "Jul","Aug","Sep","Oct","Nov","Dec"]

    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(18, 7))

    sns.boxplot(
        data=data, x="year", y="value",
        hue="year", legend=False,
        ax=ax1, palette="Set2",
        flierprops=dict(marker=".", markersize=3, alpha=0.4)
    )
    ax1.set_title("Year-wise Box Plot (Trend)")
    ax1.set_xlabel("Year")
    ax1.set_ylabel("Page Views")

    sns.boxplot(
        data=data, x="month_abbr", y="value",
        order=month_order,
        hue="month_abbr", legend=False,
        ax=ax2, palette="Set3",
        flierprops=dict(marker=".", markersize=3, alpha=0.4)
    )
    ax2.set_title("Month-wise Box Plot (Seasonality)")
    ax2.set_xlabel("Month")
    ax2.set_ylabel("Page Views")

    fig.tight_layout()
    fig.savefig("box_plot.png", dpi=150)
    print("Saved: box_plot.png")
    return fig

# ── Run all three ─────────────────────────────────────────────
